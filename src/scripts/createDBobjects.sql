#DROP TABLE tones;
CREATE TABLE tones (
    file_date BIGINT NOT NULL,
    publication_id INT NOT NULL,
    ave_tone FLOAT NULL,
    postivie_score FLOAT NULL,
    negative_score FLOAT NULL,
    polarity FLOAT NULL,
    PRIMARY KEY(file_date, publication_id)
    );

#DROP TABLE staging;
CREATE TABLE staging (
