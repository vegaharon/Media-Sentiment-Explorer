    publication_id INT NOT NULL,
    source_collection_id SMALLINT NOT NULL,
    person_id INT NULL,
    person VARCHAR(255) NULL,
    theme_id INT NULL,
    theme VARCHAR(255) NULL,
    organization_id INT NULL,
    organization VARCHAR(511) NULL
	);

